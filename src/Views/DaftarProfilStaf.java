    private void txtCariNipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCariNipActionPerformed
        if (txtCariNip.getText().length() <= 3) {
            JOptionPane.showMessageDialog(this, "Karakter Kata kunci harus lebih dari 3");
            return;
        }
        String regex = "[0-9]*";
        if (!txtCariNip.getText().matches(regex)) {
            JOptionPane.showMessageDialog(this, "Keyword haruslah NIP yaitu bilangan 0 - 9.");
            return;
        }
        sess.setTemp(txtCariNip.getText());

        tblProfil.setModel(pm.getProfilM().getUser());
    }//GEN-LAST:event_txtCariNipActionPerformed

    private void tblProfilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProfilMouseClicked
        int row = tblProfil.getSelectedRow();
        if (tblProfil.isRowSelected(row)) {
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        }
    }//GEN-LAST:event_tblProfilMouseClicked

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int row = tblProfil.getSelectedRow();
        int column = 0;
        UserSession sess = new UserSession();
        if (tblProfil.isRowSelected(row)) {
            sess.setTemp(tblProfil.getValueAt(row, column).toString());
            dispose();
            try {
                new UbahProfilStaf().setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(DaftarProfilStaf.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

