    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int columnName = 1;
        int columnNip = 0;
        int row = tblProfil.getSelectedRow();

        String nama = (String) tblProfil.getValueAt(row, columnName);
        String nip = (String) tblProfil.getValueAt(row, columnNip);
        int Pilih = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan menghapus profil " + nama + "?", "Hapus Profil", JOptionPane.OK_CANCEL_OPTION);
        sess.setTemp(nip);
        if (Pilih == JOptionPane.OK_OPTION) {
            try {
                if (pm.getProfilM().hapus() || um.getDataUser().DeleteAccount()) {
                    
                    JOptionPane.showMessageDialog(this, "Berhasil menghapus profil.");
                    tblProfil.setModel(pm.getProfilM().loadTableProfil());
                }else{
                    JOptionPane.showMessageDialog(this, "Gagal menghapus profil dan akun.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(DaftarProfilStaf.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (Pilih == JOptionPane.CANCEL_OPTION) {
            return;
        }
    }//GEN-LAST:event_btnHapusActionPerformed

